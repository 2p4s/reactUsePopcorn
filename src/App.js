function MovieDetails({ selectedId, onCloseMovie, onAddWatched, watched }) {
  const [movie, setMovie] = useState({});
  //use loading to hide the flicker whilst component loads / calls
  const [isLoading, setIsLoading] = useState(false);
  const [userRating, setUserRating] = useState("");

  //check this selected movie isnt already in the watched list
  const isWatched = watched.map((movie) => movie.imdbID).includes(selectedId);
  //console.log(isWatched);

  //use optional chaining to only take that item if it exists
  const watchedUserRating = watched.find(
    (movie) => movie.imdbID === selectedId
  )?.userRating;

  //destructure the object
  const {
    Title: title,
    Year: year,
    Poster: poster,
    Runtime: runtime,
    imdbRating,
    Plot: plot,
    Released: released,
    Actors: actors,
    Director: director,
    Genre: genre,
  } = movie;

  //will be null on first run
  console.log(title, year);

  //handler needs to do a lot so add another handler here
  function handleAdd() {
    const newWatchedMovie = {
      imdbID: selectedId,
      title,
      year,
      poster,
      imdbRating: Number(imdbRating),
      runtime: runtime.split(" ").at(0),
      userRating,
    };
    onAddWatched(newWatchedMovie);
    onCloseMovie();
  }

  //load the data
  useEffect(
    function () {
      async function getmovieDetails() {
        setIsLoading(true);
        const res = await fetch(
          `http://www.omdbapi.com/?apikey=${KEY}&i=${selectedId}`
        );
        const data = await res.json();
        //console.log(data);
        //store it in state
        setMovie(data);
        setIsLoading(false);
      }
      getmovieDetails();
    },
    [selectedId] //set dependency to monitor when it changes, then re-load
  ); //currently only running when the component mounts []!

  //change page title
  useEffect(
    function () {
      if (!title) return;
      document.title = `Movie | ${title}`;
      //cleanup function to run on unmount
      return function () {
        document.title = "usePopcorn";
      };
    },
    [title] // use the variable change as a dependency
    //need to be aware that a clean up could be necessary otherwise it could enter a race condition
    //when the component is unmounted
  );
  return (
    <div className="details">
      {isLoading ? (
        <Loader />
      ) : (
        <>
          <header>
            <button className="btn-back" onClick={onCloseMovie}>
              &larr;
            </button>
            <img src={poster} alt={`Poster of ${movie} movie`} />
            <div className="details-overview">
              <h2>{title}</h2>
              <p>
                {released} &bull; {runtime}
              </p>
              <p>{genre}</p>
              <p>
                <span>‚≠êÔ∏è</span>
                {imdbRating} IMDb rating
              </p>
            </div>
          </header>
          <section>
            <div className="rating">
              {!isWatched ? (
                <>
                  <StarRating
                    maxRating={10}
                    size={24}
                    onSetRating={setUserRating}
                  />

                  {userRating > 0 && (
                    <button className="btn-add" onClick={handleAdd}>
                      + Add to list
                    </button>
                  )}
                </>
              ) : (
                <p>
                  You rated this movie {watchedUserRating} <span>‚≠ê</span>
                </p>
              )}
            </div>
            <p>
              <em>{plot}</em>
            </p>
            <p>Starring {actors}</p>
            <p>Directed by {director}</p>
          </section>
        </>
      )}
    </div>
  );
}
